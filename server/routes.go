package server 

import (
	"github.com/justinas/alice"
	"github.com/gorilla/mux"
)

  //-------------------------------------------------------------------------------------------------------------------------//
 //----- MIDDLEWARE --------------------------------------------------------------------------------------------------------//
//-------------------------------------------------------------------------------------------------------------------------//

  //-------------------------------------------------------------------------------------------------------------------------//
 //----- ENTRY POINTS ------------------------------------------------------------------------------------------------------//
//-------------------------------------------------------------------------------------------------------------------------//

func (this *Server) routes () *mux.Router {
	mux := mux.NewRouter().StrictSlash(true)
	
	// queue - websockets
	mux.Handle ("/que", alice.New().ThenFunc(this.wssHandle))
    return mux
}
